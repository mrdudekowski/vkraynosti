/**
 * Генерирует fall/summer media constants через Vite SSR (резолв .ts импортов).
 */
import { writeFile, mkdir } from 'node:fs/promises';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createServer } from 'vite';

const rootDir = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const outDir = resolve(rootDir, 'src/constants/generated');

const SUMMER_PAIRS = {
  'summer-2': 'spring-12',
  'summer-3': 'spring-10',
  'summer-4': 'spring-11',
  'summer-5': 'spring-13',
  'summer-6': 'spring-7',
};

function remapUrls(urls, fromTourId, toTourId) {
  const fromSegment = `/tours/${fromTourId}/`;
  const toSegment = `/tours/${toTourId}/`;
  return urls.map((url) => url.replace(fromSegment, toSegment));
}

function remapPosters(posters, fromTourId, toTourId) {
  const fromSegment = `/tours/${fromTourId}/`;
  const toSegment = `/tours/${toTourId}/`;
  return Object.fromEntries(
    Object.entries(posters).map(([gridSrc, posterSrc]) => [
      gridSrc.replace(fromSegment, toSegment),
      posterSrc.replace(fromSegment, toSegment),
    ])
  );
}

function getSpringMediaExports(images, n) {
  const viewer = images[`TOUR_SPRING_${n}_GALLERY_VIEWER`];
  const grid = images[`TOUR_SPRING_${n}_GALLERY_GRID`];
  if (viewer == null || grid == null) {
    throw new Error(`Missing spring gallery exports for spring-${n}`);
  }
  const preface =
    images[`TOUR_SPRING_${n}_PREFACE_BACKGROUND`] ??
    images[`TOUR_SPRING_${n}_PREFACE_BLOCK_BACKGROUND_VIEWER`];
  const posters = images[`TOUR_SPRING_${n}_GRID_VIDEO_POSTERS`];
  const postersMobile = images[`TOUR_SPRING_${n}_GRID_VIDEO_POSTERS_MOBILE`];
  return { viewer, grid, preface, posters, postersMobile };
}

function bundleForTour(fromSpringId, toTourId, imageUrl, spring) {
  const galleryImages = remapUrls([...spring.viewer], fromSpringId, toTourId);
  const galleryGridUrls = remapUrls([...spring.grid], fromSpringId, toTourId);
  const prefaceBackgroundImageUrl =
    spring.preface != null ? remapUrls([spring.preface], fromSpringId, toTourId)[0] : undefined;
  const gridVideoPosters =
    spring.posters != null ? remapPosters(spring.posters, fromSpringId, toTourId) : undefined;
  const gridVideoPostersMobile =
    spring.postersMobile != null
      ? remapPosters(spring.postersMobile, fromSpringId, toTourId)
      : undefined;

  return {
    imageUrl,
    galleryImages,
    galleryGridUrls,
    ...(prefaceBackgroundImageUrl != null ? { prefaceBackgroundImageUrl } : {}),
    ...(gridVideoPosters != null && Object.keys(gridVideoPosters).length > 0
      ? { gridVideoPosters }
      : {}),
    ...(gridVideoPostersMobile != null && Object.keys(gridVideoPostersMobile).length > 0
      ? { gridVideoPostersMobile }
      : {}),
  };
}

function serializeValue(value) {
  return JSON.stringify(value, null, 2);
}

async function main() {
  const server = await createServer({
    root: rootDir,
    logLevel: 'error',
    server: { middlewareMode: true },
  });

  try {
    const images = await server.ssrLoadModule('/src/constants/images.ts');
    const fallTourImages = await server.ssrLoadModule('/src/constants/fallTourImages.ts');

    await mkdir(outDir, { recursive: true });

    const fallEntries = [];
    for (let n = 1; n <= 13; n += 1) {
      const fallId = `fall-${n}`;
      const springId = `spring-${n}`;
      const spring = getSpringMediaExports(images, n);
      const bundle = bundleForTour(
        springId,
        fallId,
        fallTourImages.FALL_TOUR_COVERS[fallId],
        spring
      );
      fallEntries.push(`  '${fallId}': ${serializeValue(bundle)}`);
    }

    const fallFile = `/* eslint-disable -- auto-generated by scripts/write-season-media-constants.mjs */
import type { FallTourId } from '../fallTourImages';

export type TourMediaBundle = {
  imageUrl: string;
  galleryImages: readonly string[];
  galleryGridUrls: readonly string[];
  prefaceBackgroundImageUrl?: string;
  gridVideoPosters?: Record<string, string>;
  gridVideoPostersMobile?: Record<string, string>;
};

export const FALL_TOUR_MEDIA_BY_ID = {
${fallEntries.join(',\n')}
} as const satisfies Record<FallTourId, TourMediaBundle>;
`;

    const summerEntries = [];
    for (const [summerId, springId] of Object.entries(SUMMER_PAIRS)) {
      const springNum = Number.parseInt(springId.replace('spring-', ''), 10);
      const spring = getSpringMediaExports(images, springNum);
      const springCover = images.SPRING_TOUR_COVERS[springId];
      const imageUrl = springCover.replace(`/tours/${springId}/`, `/tours/${summerId}/`);
      const bundle = bundleForTour(springId, summerId, imageUrl, spring);
      summerEntries.push(`  '${summerId}': ${serializeValue(bundle)}`);
    }

    const summerFile = `/* eslint-disable -- auto-generated by scripts/write-season-media-constants.mjs */
import type { SummerContentSourceTourId } from '../../data/seasonTourRegistry';

export type TourMediaBundle = {
  imageUrl: string;
  galleryImages: readonly string[];
  galleryGridUrls: readonly string[];
  prefaceBackgroundImageUrl?: string;
  gridVideoPosters?: Record<string, string>;
  gridVideoPostersMobile?: Record<string, string>;
};

export const SUMMER_PAIRED_TOUR_MEDIA_BY_ID = {
${summerEntries.join(',\n')}
} as const satisfies Record<SummerContentSourceTourId, TourMediaBundle>;
`;

    await writeFile(resolve(outDir, 'fallTourMedia.generated.ts'), fallFile, 'utf8');
    await writeFile(resolve(outDir, 'summerPairedTourMedia.generated.ts'), summerFile, 'utf8');
    process.stdout.write(
      `Wrote ${outDir}/fallTourMedia.generated.ts and summerPairedTourMedia.generated.ts\n`
    );
  } finally {
    await server.close();
  }
}

main().catch((error) => {
  process.stderr.write(`${error instanceof Error ? error.message : String(error)}\n`);
  process.exitCode = 1;
});
